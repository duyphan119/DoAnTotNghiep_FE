import { BlogForm, DashboardPaper } from "@/components";
import { AdminLayout } from "@/layouts";
import { blogActions } from "@/redux/slice/blogSlice";
import { useAppDispatch } from "@/redux/store";
import Head from "next/head";
import { useRouter } from "next/router";
import { useEffect } from "react";
import { UserJson } from "@/types/json";
import { UserModel } from "@/models";
import { requireAdminProps } from "@/lib";
import { GetServerSidePropsContext } from "next";

type Props = { profile: UserJson | null };

const Page = ({ profile }: Props) => {
  const router = useRouter();
  const appDispatch = useAppDispatch();

  useEffect(() => {
    const { id } = router.query;
    appDispatch(blogActions.fetchGetById(+`${id}`));
  }, [router.query]);

  return (
    <AdminLayout
      pageTitle="Chỉnh sửa bài viết"
      profile={new UserModel(profile)}
    >
      <>
        <Head>
          <title>Chỉnh sửa bài viết</title>
          <meta name="description" content="Generated by create next app" />
          <link rel="icon" href="/favicon.ico" />
        </Head>
        <DashboardPaper title="Thông tin bài viết">
          <BlogForm />
        </DashboardPaper>
      </>
    </AdminLayout>
  );
};
export const getServerSideProps = async (
  context: GetServerSidePropsContext
) => {
  return requireAdminProps(context);
};

export default Page;
